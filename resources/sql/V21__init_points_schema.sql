    approver VARCHAR(64) DEFAULT NULL COMMENT '审批人',
    approved_at DATETIME DEFAULT NULL COMMENT '审批时间',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    UNIQUE KEY uk_point_adjust_no (adjust_no),
    UNIQUE KEY uk_point_adjust_user_idempotency (user_id, idempotency_key),
    KEY idx_point_adjust_user_created_at (user_id, created_at),
    KEY idx_point_adjust_status (status)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='积分人工调整单表';
